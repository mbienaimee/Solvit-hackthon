// Function to find jobs by skills
export function findJobsBySkills(userSkills, limit = 5) {
  const skillsLower = userSkills.map(skill => skill.toLowerCase());
  
  const jobMatches = jobDatabase.map(job => {
    const jobSkillsLower = job.skills.map(skill => skill.toLowerCase());
    const matchingSkills = skillsLower.filter(skill => 
      jobSkillsLower.some(jobSkill => jobSkill.includes(skill) || skill.includes(jobSkill))
    );
    
    return {
      ...job,
      matchScore: (matchingSkills.length / Math.max(skillsLower.length, jobSkillsLower.length)) * 100,
      matchingSkills
    };
  });
  
  return jobMatches
    .filter(job => job.matchScore > 0)
    .sort((a, b) => b.matchScore - a.matchScore)
    .slice(0, limit);
}

// Function to find jobs by keywords
export function findJobsByKeywords(keywords, limit = 5) {
  const keywordsLower = keywords.toLowerCase();
  
  const jobMatches = jobDatabase.filter(job => {
    return job.title.toLowerCase().includes(keywordsLower) ||
           job.description.toLowerCase().includes(keywordsLower) ||
           job.category.toLowerCase().includes(keywordsLower) ||
           job.skills.some(skill => skill.toLowerCase().includes(keywordsLower));
  });
  
  return jobMatches.slice(0, limit);
}

