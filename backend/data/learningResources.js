// Function to get resources for a specific job
export function getResourcesForJob(jobTitle) {
  return learningResources[jobTitle] || [];
}

// Function to get resources for specific skills
export function getResourcesForSkills(skills) {
  const resources = [];
  skills.forEach(skill => {
    if (learningResources[skill]) {
      resources.push(...learningResources[skill]);
    }
  });
  
  // Remove duplicates based on title
  const uniqueResources = resources.filter((resource, index, self) =>
    index === self.findIndex(r => r.title === resource.title)
  );
  
  return uniqueResources;
}

